package com.hnisc.cmpas.package_object;

public class CourseScoreDetail {
    private String name;
    private int full_score;
    private int weight;
    private int self_score;
    private int final_score;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getFull_score() {
        return full_score;
    }

    public void setFull_score(int full_score) {
        this.full_score = full_score;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getSelf_score() {
        return self_score;
    }

    public void setSelf_score(int self_score) {
        this.self_score = self_score;
    }

    public int getFinal_score() {
        return final_score;
    }

    public void setFinal_score(int final_score) {
        this.final_score = final_score;
    }
}
